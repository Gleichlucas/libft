#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*dst;

	len = ft_strlen(s1);
	if (!(dst = (char *)malloc(len + 1)))
		return (NULL);
	dst = ft_strcpy(dst, s1);
	return (dst);
}
