#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n == -2147483648)
			ft_putstr("-2147483648");
		else
		{
			ft_putchar('-');
			ft_putnbr(-n);
		}
		return ;
	}
	if (n >= 10)
	{
		n /= 10;
		ft_putnbr(n);
	}
	ft_putchar((n % 10 + '0'));
}
