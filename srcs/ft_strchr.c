#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c == 0)
		return (s + ft_strlen(s) + 1);
	else
	{
		while (*s)
		{
			if (*s == (char)c)
				return (s);
			s++;
		}
	}
	return (NULL);
}
